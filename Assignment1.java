
public class Assignment1
{
    public static void main(String args[])
    {

        int num1 = 10, num2 = 20; 
        System.out.println("Initial value of num1 = " + num1); 
        System.out.println("Initial value of num2 = " + num2); 
        
        num1 += num2; 
        
        System.out.println("Value of num1 after += is " + num1);
         
        num1 -= num2; 
        
        System.out.println("Value of num1 after -= is " + num1);
    
        num1 *= num2; 
         
        System.out.println("Value of num1 after *= is " + num1);
        
        num1 /= num2; 
         
        System.out.println("Value of num1 after /= is " + num1);
        
        num1 %= num2; 
         
        System.out.println("Value of num1 after %= is " + num1); 
    
    
    }
}